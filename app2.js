let displayTarjetas = document.querySelector('#resto');

// metodos de entrada

let entradaDeBusqueda = document.querySelector('#elInput');
entradaDeBusqueda.addEventListener('keydown', (e) => {
    if (e.keyCode === 13) {
        e.preventDefault();
        limpiarBoton();
        buscarEn();
    }
});

let botonBuscar = document.querySelector('#elBoton');
botonBuscar.addEventListener('click', (e) => {
    e.preventDefault();
    limpiarBoton();
    buscarEn();
});

// renderizar pagina
let listaDeNegocios = document.querySelector('#resto');

for (let i = 0; i < otracosa.length; i++) {
    let negociosEncontrados = otracosa[i];
    listaDeNegocios.innerHTML +=
        ` 
    <div class="card mb-3 col-3" data-aos="zoom-in-up" data-aos-duration="1500" data-aos-delay="200">
    <img src="${negociosEncontrados.imagen}" class="card-img-top" alt="...">
    <div class="card-body">
        <h5 class="card-title text-center bg-warning p-2">${negociosEncontrados.nombre}</h5>
        <p class="card-text"><b>Direccion:</b> ${negociosEncontrados.direccion}</p>
        <p class="card-text"><b>Telefono:</b><a href="tel:${negociosEncontrados.telefono}">${negociosEncontrados.telefono} <i class="bi bi-telephone-fill"></i></a></p>
        <a href="${negociosEncontrados.html}" class="btn-menu">Menu</a>
    </div></div>
`
}

// renderizar pagina

function buscarEn() {

    let frase = document.querySelector('#elInput').value.toLowerCase();
    let palabra = frase.split(' ');

    for (let i = 0; i < palabra.length; i++) {
        const element = palabra[i];

        for (let i = 0; i < otracosa.length; i++) {
            const claves = otracosa[i].palabras;
            const restaurante = otracosa[i];

            if (claves.includes(element)) {

                displayTarjetas.innerHTML += `           
                <div class="card mb-3 col-3" data-aos="zoom-in-up" data-aos-duration="1500">
                <img src="${restaurante.imagen}" class="card-img-top" alt="...">
                    <div class="card-body">
                        <h5 class="card-title text-center bg-warning p-2">${restaurante.nombre}</h5>
                        <p class="card-text"><b>Direccion:</b> ${restaurante.direccion}</p>
                        <p class="card-text"><b>Telefono:</b><a href="tel:${restaurante.telefono}">${restaurante.telefono} <i class="bi bi-telephone-fill"></i></a></p>
                        <a href="${restaurante.html}" class="btn-menu">Menu</a>
                    </div>
                </div>
            `
            }
        }
    }
}

// limpiar entrada de busqueda

function limpiarBoton() {

    let listaDeNegocios = document.querySelector('#resto');
    listaDeNegocios.innerHTML = ' ';

}

